
#include <iostream>

using namespace std;

int main()
{

	int a=5,b=3;

	int sum =a+b;
	int diff=a-b;
	int prod=a*b;
	int quot=a/b;
	int rem=a%b;

    cout << "Sum : " << sum << endl; // 49
    cout << "Diff: " << diff << endl; // 11
    cout << "Prod: " << prod << endl; // 570
    cout << "Quot: " << quot << endl; // 1
    cout << "Rem : " << rem << endl; // 11

    return 0;
}
